package d22092025;

public class LoopFor2 {
// 10 to 0 
	
	
	public static void loop1() {
		
		for(int i=-5;i>=-15;i--) {
			System.out.println(i+" Mohammed Mohsin");
		}
	}
	
public static void loop2() {
		
	for(int i=10;i>=0;i-=1) {
		
		System.out.println(i+" Mohammed Mohsin");
		}
	}
	
	
	public static void main(String[] args) {
		
		loop1();
		loop2();
	}

}
